<template>
  <div>
    <div v-for="user in users" v-bind:key="user">{{ user }}</div>
  </div>
</template>

<script>
import axios from "axios";
export default {
  data() {
    return {
      users: [],
    };
  },
  created() {
    axios
      .get("https://api.hnpwa.com/v0/news/1.json")
      .then((response) => (this.users = response.data))
      .catch((error) => console.log(console.log(error)));
  },
};
</script>

